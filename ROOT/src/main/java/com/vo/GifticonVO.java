package com.vo;

import java.sql.Timestamp;

public class GifticonVO {

	private String giftCode;
	private String giftName;
	private String giftBrand;
	private int gitfPrice;
	private Timestamp giftDate;
	private String giftComment;
	private String giftImage;
	public String getGiftCode() {
		return giftCode;
	}
	public void setGiftCode(String giftCode) {
		this.giftCode = giftCode;
	}
	public String getGiftName() {
		return giftName;
	}
	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}
	public String getGiftBrand() {
		return giftBrand;
	}
	public void setGiftBrand(String giftBrand) {
		this.giftBrand = giftBrand;
	}
	public int getGitfPrice() {
		return gitfPrice;
	}
	public void setGitfPrice(int gitfPrice) {
		this.gitfPrice = gitfPrice;
	}
	public Timestamp getGiftDate() {
		return giftDate;
	}
	public void setGiftDate(Timestamp giftDate) {
		this.giftDate = giftDate;
	}
	public String getGiftComment() {
		return giftComment;
	}
	public void setGiftComment(String giftComment) {
		this.giftComment = giftComment;
	}
	public String getGiftImage() {
		return giftImage;
	}
	public void setGiftImage(String giftImage) {
		this.giftImage = giftImage;
	}
	
	
	
}
