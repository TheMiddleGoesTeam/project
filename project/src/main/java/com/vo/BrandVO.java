package com.vo;

public class BrandVO {
	
	private String brandCode;
	private String brandName;
	private String brandHomepage;
	private String brandComment;
	private String brandImg;
	
	
	public String getBrandCode() {
		return brandCode;
	}
	public void setBrandCode(String brandCode) {
		this.brandCode = brandCode;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getBrandHomepage() {
		return brandHomepage;
	}
	public void setBrandHomepage(String brandHomepage) {
		this.brandHomepage = brandHomepage;
	}
	public String getBrandComment() {
		return brandComment;
	}
	public void setBrandComment(String brandComment) {
		this.brandComment = brandComment;
	}
	public String getBrandImg() {
		return brandImg;
	}
	public void setBrandImg(String brandImg) {
		this.brandImg = brandImg;
	}

}
